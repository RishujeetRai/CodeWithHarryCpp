#include<iostream>
using namespace std;
int main(){
    int a, b;
    cout<<"Enter the value of int a: ";     //   '<<' is the insertion operator
    cin>>a;     //   '>>' is the extraction operator
    cout<<"Enter the value of int b: ";     //   '<<' is the insertion operator
    cin>>b;     //   '>>' is the extraction operator
    cout<<"The value of their sum is: "<<a+b;
    return 0;
}